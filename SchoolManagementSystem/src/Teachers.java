    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {

            int row = tableTeacher.getSelectedRow();
            String value = (tableTeacher.getModel().getValueAt(row, 0).toString());
            String query = "UPDATE teacher SET teacher_id= ?, teacherName = ?,courseGroupAssign = ? WHERE id = " + value;

            PreparedStatement preparedStmt = con.prepareStatement(query);

            preparedStmt.setString(1, teacher_id.getText());
            preparedStmt.setString(2, inputTname.getText());
            String course = groupCourse.getSelectedItem().toString();

            preparedStmt.setString(3, course);

            preparedStmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Updated SUCESSFULLY!");
            DefaultTableModel model = (DefaultTableModel) tableTeacher.getModel();
            model.setRowCount(0);
            showTeachers();
             update.setEnabled(false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_updateActionPerformed

