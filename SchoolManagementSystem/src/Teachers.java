    public ArrayList<Teacher> teachersList() {
        ArrayList<Teacher> teachersList = new ArrayList<>();
        try {

            String query = "SELECT * FROM teacher";
            stmt = con.createStatement();
//                PreparedStatement preparedStmt = con.prepareStatement(query);
            rs = stmt.executeQuery(query);

            Teacher teachers;
            while (rs.next()) {
                teachers = new Teacher(rs.getInt("id"), rs.getInt("teacher_id"), rs.getString("teacherName"), rs.getString("courseGroupAssign"));
                teachersList.add(teachers);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return teachersList;
    }

    public void showTeachers() {
        ArrayList<Teacher> list = teachersList();
        DefaultTableModel model = (DefaultTableModel) tableTeacher.getModel();
        Object[] row = new Object[4];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getTeacher_id();
            row[2] = list.get(i).getTeacherName();
            row[3] = list.get(i).getCourseGroupAssign();

            model.addRow(row);
        }
    }

    private void FillCombobox() {
        try {
            String sql = "SELECT * FROM course";
            PreparedStatement preparedStmt = con.prepareStatement(sql);
            rs = preparedStmt.executeQuery();

            while (rs.next()) {
                String courseGroup = rs.getString("courseGroup");
                groupCourse.addItem(courseGroup);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

